  <script>
  // Controller: läuft nach den defer-Modulen (DOMContentLoaded)
  document.addEventListener('DOMContentLoaded', function () {
    if (!window.CFArcade) return;

    const TITLES = { snake: '🐍 Snake', catcher: '🍎 Frucht-Fänger', smash: '📦 Kisten-Smash', memory: '🧠 Memory' };

    const select   = document.getElementById('gameSelect');
    const play     = document.getElementById('gamePlay');
    const stage    = document.getElementById('cf-stage');
    const hudScore = document.getElementById('hudScore');
    const hudBest  = document.getElementById('hudBest');
    const boardTitle = document.getElementById('boardTitle');
    const board    = document.getElementById('leaderboard');
    const submitRow = document.getElementById('submitRow');
    const submitScore = document.getElementById('submitScore');
    const nickInput = document.getElementById('nickInput');
    const submitBtn = document.getElementById('submitScoreBtn');

    const boardPanel  = document.getElementById('boardPanel');
    const boardResult = document.getElementById('boardResult');
    const boardResume = document.getElementById('boardResume');
    const boardRestart = document.getElementById('boardRestart');
    const boardExit   = document.getElementById('boardExit');

    let activeGame = null;
    let pendingScore = 0;
    let gameEnded = false;

    function renderBoard(scores) {
      if (!scores || !scores.length) {
        board.innerHTML = '<li class="leaderboard__empty">Noch keine Einträge – sei der Erste!</li>';
        return;
      }
      board.innerHTML = scores.map((s, i) =>
        '<li><span class="leaderboard__rank">' + (i + 1) + '.</span>' +
        '<span class="leaderboard__name">' + s.name + '</span>' +
        '<span class="leaderboard__score">' + s.score + '</span></li>'
      ).join('');
    }

    async function refreshBoard(game) {
      board.innerHTML = '<li class="leaderboard__empty">Lade…</li>';
      renderBoard(await CFArcade.loadGlobal(game));
    }

    // ── Bestenlisten-Panel (über dem Spiel) ──────────────────
    // mode: 'pause' (🏆 während des Spiels) | 'gameover' (Spielende)
    function openBoard(mode) {
      if (!activeGame) return;
      const over = mode === 'gameover';
      boardTitle.textContent = over
        ? '🏁 ' + TITLES[activeGame]
        : '🏆 Bestenliste – ' + TITLES[activeGame];
      boardResult.hidden = !over;
      submitRow.hidden = !(over && pendingScore > 0);
      boardResume.hidden = over;
      boardRestart.hidden = !over;
      nickInput.value = CFArcade.getNick();
      boardPanel.hidden = false;
      refreshBoard(activeGame);
      if (!over) CFArcade.pause();   // Spiel anhalten, solange das Panel offen ist
    }

    function closeBoard() {
      boardPanel.hidden = true;
      if (!gameEnded) CFArcade.resume();
    }

    // ── Spiel öffnen / schließen ─────────────────────────────
    function openGame(game) {
      activeGame = game;
      pendingScore = 0;
      gameEnded = false;
      select.hidden = true;
      play.hidden = false;
      boardPanel.hidden = true;
      // Vollbild-Modus: Spiel füllt den Viewport, Seite scrollt nicht
      play.classList.add('game-play--active');
      document.body.classList.add('cf-no-scroll');
      hudScore.textContent = 'Punkte 0';
      hudBest.textContent = 'Rekord ' + CFArcade.getLocalBest(game);
      stage.innerHTML = '';
      CFArcade.start(game, stage, {
        onScore(score) { hudScore.textContent = 'Punkte ' + score; },
        onGameOver(score, record) {
          gameEnded = true;
          pendingScore = score;
          hudBest.textContent = 'Rekord ' + CFArcade.getLocalBest(game);
          submitScore.textContent = score + (record ? ' 🏆 Neuer Rekord!' : '');
          // Smash-Sound/Animation kurz wirken lassen, dann Ergebnis-Panel zeigen
          setTimeout(() => openBoard('gameover'), 900);
        }
      });
      // Canvas an die neue (Vollbild-)Größe anpassen
      requestAnimationFrame(() => window.dispatchEvent(new Event('resize')));
    }

    function closeGame() {
      CFArcade.quit();
      stage.innerHTML = '';
      activeGame = null;
      gameEnded = false;
      play.hidden = true;
      boardPanel.hidden = true;
      select.hidden = false;
      play.classList.remove('game-play--active');
      document.body.classList.remove('cf-no-scroll');
    }

    function restartGame() {
      const game = activeGame;
      closeBoard();
      openGame(game);
    }

    // Esc: erst Panel schließen, sonst Spiel beenden
    document.addEventListener('keydown', e => {
      if (e.key !== 'Escape' || !activeGame) return;
      if (!boardPanel.hidden) closeBoard();
      else closeGame();
    });

    select.querySelectorAll('.game-tile[data-game]').forEach(tile =>
      tile.addEventListener('click', () => openGame(tile.dataset.game)));

    document.getElementById('closeGame').addEventListener('click', closeGame);
    document.getElementById('showBoard').addEventListener('click', () => {
      boardPanel.hidden ? openBoard(gameEnded ? 'gameover' : 'pause') : closeBoard();
    });
    boardResume.addEventListener('click', closeBoard);
    boardRestart.addEventListener('click', restartGame);
    boardExit.addEventListener('click', closeGame);

    submitBtn.addEventListener('click', async () => {
      if (!activeGame || pendingScore <= 0) return;
      const name = (nickInput.value || '').trim() || 'Anonym';
      CFArcade.setNick(name);
      submitBtn.disabled = true;
      submitBtn.textContent = 'Gesendet ✓';
      renderBoard(await CFArcade.submitGlobal(activeGame, name, pendingScore));
      pendingScore = 0;
      submitRow.hidden = true;
      setTimeout(() => { submitBtn.disabled = false; submitBtn.textContent = 'Eintragen'; }, 1500);
    });
  });
